//swaping the numbers
import java.util.Scanner;
class Inputswap2
{
public static void main(String args[])
{
Scanner sc =new Scanner(System.in);
int num1; int num2;
num1=sc.nextInt();//8
num2=sc.nextInt();//9
num1=num1+num2;//17
num1=num1-num2;//8
num2=num1-num2;//-1
System.out.println(num1+"\n"+num2);
}//main()
}//Inputswap